function sqlEscape(s) {
  return s.replace(/'/g, "''");
}

async function main() {
  const raw = await fs.readFile(JSON_PATH, 'utf8');
  const data = JSON.parse(raw);

  const sqlLines = [
    '-- 필름 소개글 어색한 AI 어투 정리.',
    '-- Supabase Studio SQL Editor 에 통째로 붙여넣고 Run.',
    '-- 적용 후 Netlify 재빌드 트리거 (사소한 커밋 push 또는 Netlify Deploy 버튼) 필요.',
    '',
    'BEGIN;',
    '',
  ];

  let changed = 0;
  let missing = [];
  for (const [slug, newDesc] of Object.entries(REPLACEMENTS)) {
    if (!data[slug]) {
      missing.push(slug);
      continue;
    }
    if (data[slug].desc === newDesc) continue;
    data[slug].desc = newDesc;
    sqlLines.push(`UPDATE public.films SET description = '${sqlEscape(newDesc)}' WHERE slug = '${slug}';`);
    changed++;
  }

  sqlLines.push('', 'COMMIT;', '');

  await fs.writeFile(JSON_PATH, JSON.stringify(data, null, 2) + '\n', 'utf8');
  await fs.writeFile(SQL_PATH, sqlLines.join('\n'), 'utf8');

  console.log(`✔ ${changed} 개 필름 소개글 갱신`);
  if (missing.length) console.warn('⚠ 매핑에 있으나 JSON 에 없는 slug:', missing.join(', '));
  console.log(`→ data/films.json 갱신`);
  console.log(`→ ${path.relative(ROOT, SQL_PATH)} 생성 (Supabase 에 적용 필요)`);
}

main().catch(err => {
  console.error('실패:', err);
  process.exit(1);
});
